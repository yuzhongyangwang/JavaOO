package practice07;

public class Klass {
    private Integer klass;

    public Klass(Integer klass) {
        this.klass = klass;
    }

    public Integer getKlass() {
        return klass;
    }

    public void setKlass(Integer klass) {
        this.klass = klass;
    }
    public Integer getNumber() {
        return klass;
    }
    public String getDisplayName() {
        return "Class "+klass;
    }

}
